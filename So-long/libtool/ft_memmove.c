#include "libft.h"

void	*ft_memmove(void *dst, const void *src, size_t len)
{
	char	*src1;
	char	*dst1;
	size_t	i;

	i = 0;
	src1 = (char *)src;
	dst1 = (char *)dst;
	if (dst1 > src1)
	{
		while (len > 0)
		{
			len--;
			dst1[len] = src1[len];
		}
	}
	else
		ft_memcpy(dst1, src1, len);
	return (dst);
}
